#include "libft.h"

void	ft_print_hex(long long int nbr)
{
	if (nbr >= 16)
		ft_print_hex(nbr / 16);
	nbr = nbr % 16;
	nbr += (nbr < 10) ? '0' : 'a' - 10;
	write(1, &nbr, 1);
}